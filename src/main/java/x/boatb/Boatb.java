package x.boatb;

public class Boatb {

    private String name;
    private int maxPassengers;

    public Boatb(String name, int maxPassengers) {
        this.name = name;
        this.maxPassengers = maxPassengers;
    }

    public String getName() {
        return name;
    }

    public int getMaxPassengers() {
        return maxPassengers;
    }
}
